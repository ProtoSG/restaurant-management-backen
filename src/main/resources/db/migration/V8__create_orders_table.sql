CREATE TABLE orders (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_code      VARCHAR(50)     NOT NULL UNIQUE,
    table_id        BIGINT          REFERENCES tables(id),
    waiter_id       BIGINT          REFERENCES users(id),
    type            VARCHAR(20)     NOT NULL DEFAULT 'DINE_IN',
    customer_name   VARCHAR(255),
    status          VARCHAR(20)     NOT NULL DEFAULT 'CREATED',
    total           NUMERIC(10,2)   NOT NULL DEFAULT 0.00,
    discount        NUMERIC(10,2)   NOT NULL DEFAULT 0.00,
    closed_at       TIMESTAMP,
    date_created    TIMESTAMP,
    date_updated    TIMESTAMP,
    user_created    VARCHAR(100),
    user_updated    VARCHAR(100),
    CONSTRAINT chk_orders_status   CHECK (status IN ('CREATED', 'IN_PROGRESS', 'READY', 'PARTIALLY_PAID', 'PAID', 'CANCELLED')),
    CONSTRAINT chk_orders_type     CHECK (type   IN ('DINE_IN', 'TAKEAWAY', 'DELIVERY')),
    CONSTRAINT chk_orders_total    CHECK (total >= 0),
    CONSTRAINT chk_orders_discount CHECK (discount >= 0)
);
